    <p>
    La principale problématique du Mont sur lequel repose l’abbaye est que sa configuration ne permet pas la construction de bâtiments côte à côte par manque d’espace. C’est pour cela que l’abbaye a été construite sur trois niveaux. Le Mont est entouré de remparts massifs, de 7 tours de défenses et de portes très lourdement fortifiées. C’est là, toute la puissance défensive du Mont-Saint-Michel, qui aura permit de résister à plusieurs attaques en période de guerre. <br><br>

    Afin de relier les différents bâtiments entre eux, des couloirs et escaliers ont été construits malgré la complexité de la forme du Mont. Ainsi, les chemins forment un désordre dans l’abbaye qui peut faire penser à un labyrinthe. L’aspect physique de l’abbaye montre une hiérarchie religieuse : les bâtiments les moins spirituels étaient situés au premier niveau comme le cellier qui stockait les vivres par exemple. <br>
    Plus l’on gravit le mont, plus les bâtiments deviennent symboliques dans la religion jusqu'à arriver à la Merveille, se situant tout en haut du Mont. La hauteur était signe de foi, mais aussi de pouvoir, là où le clergé avait de grands pouvoirs. <br><br>

    Plus à l’ouest se trouve la partie habitation. Elle a la fonction d’hôtellerie de par ses hôtels, ses logis, son infirmerie et sa chapelle Saint-Étienne. Le tout présent au premier et second niveaux. <br><br>

