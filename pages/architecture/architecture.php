    <h2>Notre-Dame-Sous-Terre</h2>

    <p>
        Notre-Dame-Sous-Terre est une église carolingienne qui a été construite entre l’an 900 et l’an 1100. Elle a la spécificité d’être le plus ancien édifice de l'abbaye. Elle est constituée de larges murs de 2 mètres d’épaisseur de granite et d’arcs de briques plates en granite. L’église est soutenue par plusieurs rangées de piliers qui font également partie du soutien de la Merveille. Les coins de l’église sont sombres, car la lumière passe principalement par les vaisseaux qui viennent éclairer le centre de la nef. L’architecture adoptée à cet édifice est le style préroman. <br><br>
    </p>

